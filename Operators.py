# Arithmetic Operators
x = 10
y = 5

print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x % y)# Modulus - Returns the remainder

#Comparizon Operators
print(x < y)
print(x > y)
print(x <= y)
print(x >= y)
print(x == y) # Equal to
print(x != y) # Not equal to

#Assignment Operators
a = 30
print(a)

a += 2
print(a)

#Operator Precedence
number = (45 - 2 * 3 + 5)
print(number)

#Logical Operators - and, or, not
print(55 < 234 and 67 < 23)
print(55 < 234 or 67 < 23)
print(not (55 < 234 and 67 < 23))

